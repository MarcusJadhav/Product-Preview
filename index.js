function changeImg (num) {
    const currentImg = document.getElementById("main-img")

    if(num == 1)
    {
        currentImg.src="./images/1.jpg"
    }
    else if (num == 2) {
        currentImg.src="./images/2.jpg"
    }
    else if (num == 3) {
        currentImg.src="./images/3.jpg"
    }
    else if (num == 4) {
        currentImg.src="./images/4.jpg"
    }
    else if (num == 5) {
        currentImg.src="./images/5.jpg"
    }
}